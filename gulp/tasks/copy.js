// Чтобы функцию могли использовать, то её тоже экспорт
export const copy = () => {
    // встроенный метод src, обращаемся к глобальной переменной app. там path и в самой переменной path (в path.js)
    // есть src > files
    // файл получен и теперь необходимо действие
    return app.gulp.src(app.path.src.files) // получили файлы
        // дейсвтие:
        .pipe(app.gulp.dest(app.path.build.files)) // перенесли файлы
}
